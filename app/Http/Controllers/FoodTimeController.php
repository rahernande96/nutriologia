    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->ajax())
        {
            $params = array();
            parse_str($request->dataJson, $params);

            if(FoodTime::create([
                'name'  => $params['name']
            ]))
            {
                $food_time = FoodTime::all()->last();
                return response()->json(['status' => 1, 'foodTime'  => $food_time]);
            }
            else
            {
                return response()->json(['status' => 0, 'message' => 'El tiempo de comida no se pudo crear']);
            }
        }
    }

